        document.addEventListener('DOMContentLoaded', function() {
            // Set initial language to English
            setLanguage('en');
            
            const form = document.getElementById('registration-form');
            
            form.addEventListener('submit', function(e) {
                e.preventDefault();
                
                // Basic validation
                let isValid = true;
                const requiredFields = form.querySelectorAll('input[required]');
                
                requiredFields.forEach(field => {
                    if (!field.value.trim()) {
                        isValid = false;
                        field.style.borderColor = 'red';
                    } else {
                        field.style.borderColor = '#ddd';
                    }
                });
                
                const agreeCheckbox = document.getElementById('agree');
                if (!agreeCheckbox.checked) {
                    isValid = false;
                    alert('You must agree to the terms and conditions');
                }
                
                if (isValid) {
                    // Animation for successful submission
                    const submitBtn = document.querySelector('.submit-btn');
                    const originalText = submitBtn.textContent;
                    submitBtn.textContent = 'Submitting...';
                    submitBtn.style.opacity = '0.8';
                    
                    // Simulate form submission
                    setTimeout(() => {
                        submitBtn.textContent = 'Registration Successful!';
                        submitBtn.style.background = 'linear-gradient(to right, #00b09b, #96c93d)';
                        
                        // Reset form after success
                        setTimeout(() => {
                            form.reset();
                            submitBtn.textContent = originalText;
                            submitBtn.style.background = 'linear-gradient(to right, #1a2a6c, #b21f1f)';
                            submitBtn.style.opacity = '1';
                            alert('Registration submitted successfully!');
                        }, 1500);
                    }, 2000);
                }
            });
            
            // Add animation to form elements on scroll
            const observerOptions = {
                root: null,
                rootMargin: '0px',
                threshold: 0.1
            };
            
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.style.animation = 'fadeIn 1s ease-out forwards';
                        observer.unobserve(entry.target);
                    }
                });
            }, observerOptions);
            
            document.querySelectorAll('.form-section, .terms-section').forEach(section => {
                section.style.opacity = '0';
                observer.observe(section);
            });
        });